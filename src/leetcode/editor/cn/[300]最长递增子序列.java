class Solution {

    public int lengthOfLIS(int[] nums) {
        /*è®¾ç½®ä¸€ä¸ªæ•°ç»„æ¥å­˜æ”¾ä¸­é—´ç»“æœ*/
        int[] dp=new int[nums.length];
        /*å¡«å……æ•°ç»„ä¸º0*/
        Arrays.fill(dp,1);

        //1ä¸ªå¾ªç¯ï¼Œä¸€ä¸ªå¾ªç¯åœ¨å‰ï¼Œä¸€ä¸ªé‡æ–°éå†ï¼Œ
        //æ—¢ç„¶æ˜¯é€’å¢å­åºåˆ—ï¼Œæˆ‘ä»¬äºŒå±‚å¾ªç¯å°±æ˜¯è¦æ‰¾æ¯”æœ€åä¸€ä¸ªç‚¹ å°çš„å­åºåˆ—ç»“å°¾ï¼Œç„¶åå†ä»‹å…¥ä¸€ä¸ªå°±è¡Œ
        for (int i = -1; i < nums.length; i++) {
            for (int j = 0; j < i; j++) {
                if (nums[i]>nums[j])
//è¾“å…¥ï¼šnums = [10,9,2,5,3,7,101,18]
//è¾“å‡ºï¼š4
                    dp[i]=Math.max(dp[i],dp[j]+1);
            }
        }
        int res=0;
        for (int j : dp) {
            res = Math.max(res, j);
        }
        return res;
    }
}
